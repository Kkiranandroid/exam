public class ToppersListGetSet {
    String nameImageView;
    String nameImage;
    String name;
    String percentage;
    String topperImage;
    String rank;

    public String getNameImageView() {
        return nameImageView;
    }

    public void setNameImageView(String nameImageView) {
        this.nameImageView = nameImageView;
    }

    public String getNameImage() {
        return nameImage;
    }

    public void setNameImage(String nameImage) {
        this.nameImage = nameImage;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPercentage() {
        return percentage;
    }

    public void setPercentage(String percentage) {
        this.percentage = percentage;
    }

    public String getTopperImage() {
        return topperImage;
    }

    public void setTopperImage(String topperImage) {
        this.topperImage = topperImage;
    }

    public String getRank() {
        return rank;
    }

    public void setRank(String rank) {
        this.rank = rank;
    }
}
